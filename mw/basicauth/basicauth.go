func (s *JSONStorage) Reload() error {
	users, err := LoadUsersJSON(s.path)
	if err != nil {
		return err
	}

	next := make(map[string]User, len(users))
	for _, user := range users {
		next[user.Username] = cloneUser(user)
	}

	s.mu.Lock()
	s.users = next
	s.mu.Unlock()

	return nil
}

func LoadUsersJSON(path string) ([]User, error) {
	b, err := os.ReadFile(path)
	if err != nil {
		return nil, err
	}

	var wrapped jsonUsersFile
	if err := json.Unmarshal(b, &wrapped); err == nil && wrapped.Users != nil {
		return validateUsers(wrapped.Users)
	}

	var direct []User
	if err := json.Unmarshal(b, &direct); err != nil {
		return nil, err
	}

	return validateUsers(direct)
}

func SaveUsersJSON(path string, users []User) error {
	b, err := json.MarshalIndent(jsonUsersFile{Users: users}, "", "  ")
	if err != nil {
		return err
	}
	return os.WriteFile(path, b, 0600)
}

func validateUsers(users []User) ([]User, error) {
	for i := range users {
		if users[i].Username == "" {
			return nil, fmt.Errorf("basicauth: empty username at user index %d", i)
		}
		if users[i].PasswordHash == "" {
			return nil, fmt.Errorf("basicauth: empty password_hash for user %q", users[i].Username)
		}
	}
	return users, nil
}

// -----------------------------------------------------------------------------
// PBKDF2-SHA256 hasher
// -----------------------------------------------------------------------------

type PBKDF2Hasher struct {
	Iterations int
	SaltBytes  int
	KeyBytes   int
}

func DefaultHasher() *PBKDF2Hasher {
	return &PBKDF2Hasher{
		Iterations: 150000,
		SaltBytes:  16,
		KeyBytes:   32,
	}
}

func HashPassword(password string) (string, error) {
	return DefaultHasher().Hash(password)
}

func MustHashPassword(password string) string {
	hash, err := HashPassword(password)
	if err != nil {
		panic(err)
	}
	return hash
}

func (h *PBKDF2Hasher) Hash(password string) (string, error) {
	iterations := h.Iterations
	if iterations <= 0 {
		iterations = 150000
	}

	saltBytes := h.SaltBytes
	if saltBytes <= 0 {
		saltBytes = 16
	}

	keyBytes := h.KeyBytes
	if keyBytes <= 0 {
		keyBytes = 32
	}

	salt := make([]byte, saltBytes)
	if _, err := rand.Read(salt); err != nil {
		return "", err
	}

	key := pbkdf2Key([]byte(password), salt, iterations, keyBytes, sha256.New)

	return hashPrefix +
		"$" + strconv.Itoa(iterations) +
		"$" + base64.RawStdEncoding.EncodeToString(salt) +
		"$" + base64.RawStdEncoding.EncodeToString(key), nil
}

func (h *PBKDF2Hasher) Verify(password string, encodedHash string) bool {
	parts := strings.Split(encodedHash, "$")
	if len(parts) != 4 {
		return false
	}

	if subtle.ConstantTimeCompare([]byte(parts[0]), []byte(hashPrefix)) != 1 {
		return false
	}

	iterations, err := strconv.Atoi(parts[1])
	if err != nil || iterations <= 0 {
		return false
	}

	salt, err := base64.RawStdEncoding.DecodeString(parts[2])
	if err != nil || len(salt) == 0 {
		return false
	}

	expected, err := base64.RawStdEncoding.DecodeString(parts[3])
	if err != nil || len(expected) == 0 {
		return false
	}

	actual := pbkdf2Key([]byte(password), salt, iterations, len(expected), sha256.New)

	return subtle.ConstantTimeCompare(actual, expected) == 1
}

func pbkdf2Key(password, salt []byte, iter, keyLen int, h func() hash.Hash) []byte {
	prf := hmac.New(h, password)
	hashLen := prf.Size()
	numBlocks := (keyLen + hashLen - 1) / hashLen

	var buf [4]byte
	dk := make([]byte, 0, numBlocks*hashLen)
	u := make([]byte, hashLen)

	for block := 1; block <= numBlocks; block++ {
		prf.Reset()
		prf.Write(salt)
		binary.BigEndian.PutUint32(buf[:], uint32(block))
		prf.Write(buf[:])
		sum := prf.Sum(nil)

		t := make([]byte, hashLen)
		copy(t, sum)
		copy(u, sum)

		for i := 2; i <= iter; i++ {
			prf.Reset()
			prf.Write(u)
			u = prf.Sum(u[:0])

			for x := 0; x < hashLen; x++ {
				t[x] ^= u[x]
			}
		}

		dk = append(dk, t...)
	}

	return dk[:keyLen]
}

// -----------------------------------------------------------------------------
// Helpers
// -----------------------------------------------------------------------------

func splitList(s string) []string {
	if s == "" {
		return nil
	}

	fields := strings.FieldsFunc(s, func(r rune) bool {
		return r == '|' || r == ',' || r == ';'
	})

	out := make([]string, 0, len(fields))
	for _, f := range fields {
		f = strings.TrimSpace(f)
		if f != "" {
			out = append(out, f)
		}
	}

	return out
}

func cloneUser(user User) User {
	return User{
		Username:     user.Username,
		PasswordHash: user.PasswordHash,
		Enabled:      user.Enabled,
		Roles:        cloneStrings(user.Roles),
		Metadata:     cloneMap(user.Metadata),
	}
}

func cloneStrings(in []string) []string {
	if len(in) == 0 {
		return nil
	}
	out := make([]string, len(in))
	copy(out, in)
	return out
}

func cloneMap(in map[string]string) map[string]string {
	if len(in) == 0 {
		return nil
	}
	out := make(map[string]string, len(in))
	for k, v := range in {
		out[k] = v
	}
	return out
}
