//
// CSV format:
//
// username,password_hash,enabled,roles
// admin,pbkdf2-sha256$150000$...$...,true,admin|manager
//
// Header row is optional.

type CSVStorage struct {
	path string

	mu    sync.RWMutex
	users map[string]User
}

func NewCSVStorage(path string) (*CSVStorage, error) {
	s := &CSVStorage{
		path:  path,
		users: make(map[string]User),
	}
	if err := s.Reload(); err != nil {
		return nil, err
	}
	return s, nil
}

func (s *CSVStorage) GetUser(username string) (User, bool, error) {
	s.mu.RLock()
	user, ok := s.users[username]
	s.mu.RUnlock()

	if !ok {
		return User{}, false, nil
	}

	return cloneUser(user), true, nil
}

func (s *CSVStorage) Reload() error {
	users, err := LoadUsersCSV(s.path)
	if err != nil {
		return err
	}

	next := make(map[string]User, len(users))
	for _, user := range users {
		next[user.Username] = cloneUser(user)
	}

	s.mu.Lock()
	s.users = next
	s.mu.Unlock()

	return nil
}

func LoadUsersCSV(path string) ([]User, error) {
	f, err := os.Open(path)
	if err != nil {
		return nil, err
	}
	defer f.Close()

	r := csv.NewReader(f)
	r.TrimLeadingSpace = true
	r.FieldsPerRecord = -1

	var users []User
	rowIndex := 0

	for {
		row, err := r.Read()
		if errors.Is(err, io.EOF) {
			break
		}
		if err != nil {
			return nil, err
		}

		rowIndex++

		if len(row) == 0 {
			continue
		}

		for i := range row {
			row[i] = strings.TrimSpace(row[i])
		}

		if rowIndex == 1 && isCSVHeader(row) {
			continue
		}

		if len(row) < 2 {
			return nil, fmt.Errorf("basicauth: invalid CSV row %d: expected at least username,password_hash", rowIndex)
		}

		username := row[0]
		passwordHash := row[1]

		if username == "" {
			return nil, fmt.Errorf("basicauth: empty username at CSV row %d", rowIndex)
		}
		if passwordHash == "" {
			return nil, fmt.Errorf("basicauth: empty password_hash at CSV row %d", rowIndex)
		}

		enabled := true
		if len(row) >= 3 && row[2] != "" {
			enabled, err = strconv.ParseBool(row[2])
			if err != nil {
				return nil, fmt.Errorf("basicauth: invalid enabled value at CSV row %d: %w", rowIndex, err)
			}
		}

		var roles []string
		if len(row) >= 4 && row[3] != "" {
			roles = splitList(row[3])
		}

		users = append(users, User{
			Username:     username,
			PasswordHash: passwordHash,
			Enabled:      enabled,
			Roles:        roles,
		})
	}

	return users, nil
}

func SaveUsersCSV(path string, users []User) error {
	f, err := os.Create(path)
	if err != nil {
		return err
	}
	defer f.Close()

	w := csv.NewWriter(f)

	if err := w.Write([]string{"username", "password_hash", "enabled", "roles"}); err != nil {
		return err
	}

	for _, user := range users {
		row := []string{
			user.Username,
			user.PasswordHash,
			strconv.FormatBool(user.Enabled),
			strings.Join(user.Roles, "|"),
		}
		if err := w.Write(row); err != nil {
			return err
		}
	}

	w.Flush()
	return w.Error()
}

func isCSVHeader(row []string) bool {
	if len(row) < 2 {
		return false
	}
	return strings.EqualFold(row[0], "username") &&
		(strings.EqualFold(row[1], "password_hash") || strings.EqualFold(row[1], "password"))
}

