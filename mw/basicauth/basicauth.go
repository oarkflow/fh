const (
	defaultRealm          = "Restricted"
	defaultMaxHeaderBytes = 4096

	hashPrefix = "pbkdf2-sha256"
)

var (
	ErrUserNotFound      = errors.New("basicauth: user not found")
	ErrInvalidCredential = errors.New("basicauth: invalid credential")
	ErrInvalidHash       = errors.New("basicauth: invalid password hash")
	ErrDisabledUser      = errors.New("basicauth: disabled user")
)

// User is the stored user record.
// PasswordHash should contain a PBKDF2 hash generated by HashPassword.
type User struct {
	Username     string            `json:"username"`
	PasswordHash string            `json:"password_hash"`
	Enabled      bool              `json:"enabled"`
	Roles        []string          `json:"roles,omitempty"`
	Metadata     map[string]string `json:"metadata,omitempty"`
}

// Principal is the authenticated user exposed to callbacks.
type Principal struct {
	Username string
	Roles    []string
	Metadata map[string]string
}

// Storage is the pluggable user backend.
//
// Implementations may use memory, CSV, JSON, database, Redis, etc.
// The middleware only requires GetUser.
type Storage interface {
	GetUser(username string) (User, bool, error)
}

// Reloader is optional for file-backed stores.
type Reloader interface {
	Reload() error
}

// PasswordHasher verifies and creates password hashes.
type PasswordHasher interface {
	Hash(password string) (string, error)
	Verify(password string, encodedHash string) bool
}

// UnauthorizedHandler allows custom unauthorized responses.
type UnauthorizedHandler func(ctx *fh.Ctx, realm string) error

// AuthenticatedHandler runs after successful authentication and before ctx.Next().
type AuthenticatedHandler func(ctx *fh.Ctx, principal Principal) error

// Config controls the middleware behavior.
type Config struct {
	Realm string

	Storage Storage
	Hasher  PasswordHasher

	// MaxHeaderBytes prevents very large Authorization headers from wasting CPU/memory.
	// Default: 4096.
	MaxHeaderBytes int

	// RequireTLS rejects Basic Auth unless the request appears to be HTTPS.
	// Recommended in production behind TLS.
	RequireTLS bool

	// TrustProxyHeaders allows X-Forwarded-Proto / X-Forwarded-Ssl checks.
	// Enable only if your app is behind a trusted reverse proxy.
	TrustProxyHeaders bool

	UnauthorizedHandler UnauthorizedHandler
	OnAuthenticated     AuthenticatedHandler

	// DummyHash is used when a username does not exist.
	// This reduces username enumeration through timing differences.
	DummyHash string
}

// New keeps your old API working.
func New(username, password string) fh.HandlerFunc {
	hasher := DefaultHasher()
	hash, err := hasher.Hash(password)
	if err != nil {
		panic(err)
	}

	store := NewMemoryStorage()
	store.Set(User{
		Username:     username,
		PasswordHash: hash,
		Enabled:      true,
	})

	return NewWithConfig(Config{
		Storage: store,
		Hasher:  hasher,
		Realm:   defaultRealm,
	})
}

// NewWithConfig creates the Basic Auth middleware.
func NewWithConfig(config Config) fh.HandlerFunc {
	cfg := normalizeConfig(config)

	return func(ctx *fh.Ctx) error {
		if cfg.RequireTLS && !isHTTPS(ctx, cfg.TrustProxyHeaders) {
			return cfg.UnauthorizedHandler(ctx, cfg.Realm)
		}

		auth := ctx.Get("Authorization")
		if auth == "" || len(auth) > cfg.MaxHeaderBytes {
			return cfg.UnauthorizedHandler(ctx, cfg.Realm)
		}

		username, password, ok := parseBasicAuth(auth)
		if !ok {
			return cfg.UnauthorizedHandler(ctx, cfg.Realm)
		}

		user, found, err := cfg.Storage.GetUser(username)
		if err != nil {
			return cfg.UnauthorizedHandler(ctx, cfg.Realm)
		}

		if !found {
			// Verify against dummy hash to reduce username enumeration timing leaks.
			cfg.Hasher.Verify(password, cfg.DummyHash)
			return cfg.UnauthorizedHandler(ctx, cfg.Realm)
		}

		if !user.Enabled {
			// Still verify once to reduce observable timing difference for disabled users.
			cfg.Hasher.Verify(password, user.PasswordHash)
			return cfg.UnauthorizedHandler(ctx, cfg.Realm)
		}

		if !cfg.Hasher.Verify(password, user.PasswordHash) {
			return cfg.UnauthorizedHandler(ctx, cfg.Realm)
		}

		if cfg.OnAuthenticated != nil {
			err := cfg.OnAuthenticated(ctx, Principal{
				Username: user.Username,
				Roles:    cloneStrings(user.Roles),
				Metadata: cloneMap(user.Metadata),
			})
			if err != nil {
				return err
			}
		}

		return ctx.Next()
	}
}

func normalizeConfig(cfg Config) Config {
	if cfg.Realm == "" {
		cfg.Realm = defaultRealm
	}
	if cfg.MaxHeaderBytes <= 0 {
		cfg.MaxHeaderBytes = defaultMaxHeaderBytes
	}
	if cfg.Hasher == nil {
		cfg.Hasher = DefaultHasher()
	}
	if cfg.Storage == nil {
		cfg.Storage = NewMemoryStorage()
	}
	if cfg.UnauthorizedHandler == nil {
		cfg.UnauthorizedHandler = DefaultUnauthorizedHandler
	}
	if cfg.DummyHash == "" {
		hash, err := cfg.Hasher.Hash("invalid-password")
		if err != nil {
			panic(err)
		}
		cfg.DummyHash = hash
	}
	return cfg
}

func DefaultUnauthorizedHandler(ctx *fh.Ctx, realm string) error {
	ctx.Set("WWW-Authenticate", `Basic realm="`+escapeRealm(realm)+`", charset="UTF-8"`)
	return ctx.Status(401).SendString("Unauthorized")
}

func escapeRealm(realm string) string {
	realm = strings.ReplaceAll(realm, `\`, `\\`)
	realm = strings.ReplaceAll(realm, `"`, `\"`)
	return realm
}

func parseBasicAuth(header string) (username string, password string, ok bool) {
	if len(header) < 7 {
		return "", "", false
	}

	// Scheme is case-insensitive.
	if !equalFoldASCII(header[:6], "Basic ") {
		return "", "", false
	}

	encoded := strings.TrimSpace(header[6:])
	if encoded == "" {
		return "", "", false
	}

	decodedLen := base64.StdEncoding.DecodedLen(len(encoded))
	buf := make([]byte, decodedLen)

	n, err := base64.StdEncoding.Decode(buf, []byte(encoded))
	if err != nil {
		return "", "", false
	}

	buf = buf[:n]

	colon := bytes.IndexByte(buf, ':')
	if colon <= 0 {
		return "", "", false
	}

	username = string(buf[:colon])
	password = string(buf[colon+1:])

	if username == "" {
		return "", "", false
	}

	return username, password, true
}

func equalFoldASCII(a, b string) bool {
	if len(a) != len(b) {
		return false
	}
	for i := 0; i < len(a); i++ {
		aa := a[i]
		bb := b[i]

		if 'A' <= aa && aa <= 'Z' {
			aa += 'a' - 'A'
		}
		if 'A' <= bb && bb <= 'Z' {
			bb += 'a' - 'A'
		}
		if aa != bb {
			return false
		}
	}
	return true
}

func isHTTPS(ctx *fh.Ctx, trustProxy bool) bool {
	// This keeps compatibility with custom fasthttp-like contexts.
	// If your Ctx exposes TLS state directly, you can customize this logic.

	if trustProxy {
		if strings.EqualFold(ctx.Get("X-Forwarded-Proto"), "https") {
			return true
		}
		if strings.EqualFold(ctx.Get("X-Forwarded-Ssl"), "on") {
			return true
		}
	}

	return false
}

// -----------------------------------------------------------------------------
// Memory storage
// -----------------------------------------------------------------------------

type MemoryStorage struct {
	mu    sync.RWMutex
	users map[string]User
}

func NewMemoryStorage() *MemoryStorage {
	return &MemoryStorage{
		users: make(map[string]User),
	}
}

func NewMemoryStorageFromUsers(users []User) *MemoryStorage {
	s := NewMemoryStorage()
	for _, u := range users {
		s.Set(u)
	}
	return s
}

func NewMemoryStorageFromPlain(users map[string]string) (*MemoryStorage, error) {
	hasher := DefaultHasher()
	s := NewMemoryStorage()

	for username, password := range users {
		hash, err := hasher.Hash(password)
		if err != nil {
			return nil, err
		}
		s.Set(User{
			Username:     username,
			PasswordHash: hash,
			Enabled:      true,
		})
	}

	return s, nil
}

func (s *MemoryStorage) GetUser(username string) (User, bool, error) {
	s.mu.RLock()
	user, ok := s.users[username]
	s.mu.RUnlock()

	if !ok {
		return User{}, false, nil
	}

	return cloneUser(user), true, nil
}

func (s *MemoryStorage) Set(user User) {
	s.mu.Lock()
	if s.users == nil {
		s.users = make(map[string]User)
	}
	s.users[user.Username] = cloneUser(user)
	s.mu.Unlock()
}

func (s *MemoryStorage) Delete(username string) {
	s.mu.Lock()
	delete(s.users, username)
	s.mu.Unlock()
}

func (s *MemoryStorage) Users() []User {
	s.mu.RLock()
	out := make([]User, 0, len(s.users))
	for _, user := range s.users {
		out = append(out, cloneUser(user))
	}
	s.mu.RUnlock()
	return out
}

